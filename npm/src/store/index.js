import Vue from 'vue'
import Vuex from 'vuex'

Vue.use(Vuex)

export default new Vuex.Store({

  /////////////////////////////////////////////////////////////////////////////
  // State
  /////////////////////////////////////////////////////////////////////////////

  state: {
    requester: {
      id: 0,
      avatar: null,
      name: null,
      email: null
    }
  },

  /////////////////////////////////////////////////////////////////////////////
  // Mutations
  /////////////////////////////////////////////////////////////////////////////

  mutations: {
    SET_REQUESTER(state, { id, name, email, avatar }) {
      state.requester = { id, name, email, avatar }
    }
  },

  /////////////////////////////////////////////////////////////////////////////
  // Actions
  /////////////////////////////////////////////////////////////////////////////

  actions: {},

  /////////////////////////////////////////////////////////////////////////////
  // Modules
  /////////////////////////////////////////////////////////////////////////////

  modules: {}

})
